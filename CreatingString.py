           # Creating a String

#Strings in Python can be created using single quotes or double quotes or even triple quotes

  #string with ' single quotes'
String1 = 'Vinu Prasad is a Python Devloper'
print("\nthe string with single quotes is:-",String1)

# string with "double quotes "
String1 = "Vinu Prasad is a Python Devloper"
print("\nstring with double quotes is :-",String1)

# String with triple quotes
String1 = '''Vinu Prasad is a Python Devloper'''
print("\n string with triple quotes:-",String1)

