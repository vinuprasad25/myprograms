
          #Slicing String in Python
#To access a range of characters in the String, method of slicing is used.
# Slicing in a String is done by using a Slicing operator(colon)

  #creating initial String

String1=("Vinu Prasad")
print("the initial string is:-",String1)

#slicing the string from 5 to 10
print("the slicing string is:-",String1[5:10])

#slicing the string from reverse order
print("the slicing string is:-",String1[-7:-2])

#slicing first 3 char and last 5 character from string
print("slicing string is:-",String1[3:-1])
